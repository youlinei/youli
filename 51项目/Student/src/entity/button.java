package entity;

public class button {
	private int id;
	private String button;
	public button(){
		
		
		
	}
	public button(int id,String button) {
		this.id=id;
		this.button=button;
	
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getButton() {
		return button;
	}
	public void setButton(String button) {
		this.button = button;
	}
	
	

}
